COMMENT ON COLUMN EMPLOYEES.EMPNO IS '사원번호';

COMMENT ON COLUMN EMPLOYEES.EMPNAME IS '사원이름';

COMMENT ON COLUMN EMPLOYEES.SSN IS '주민번호';

COMMENT ON COLUMN EMPLOYEES.MOBILE IS '전화번호';

COMMENT ON COLUMN EMPLOYEES.REG_DATE IS '입사일자';

COMMENT ON COLUMN EMPLOYEES.DEPTNO IS '부서번호';

CREATE UNIQUE INDEX PK_EMPLOYEES
	ON EMPLOYEES (
		EMPNO ASC
	);

ALTER TABLE EMPLOYEES
	ADD
		CONSTRAINT PK_EMPLOYEES
		PRIMARY KEY (
			EMPNO
		);

