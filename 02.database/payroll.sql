CREATE TABLE DEPARTMENTS (
	DEPTNO NUMBER NOT NULL,
	DEPTNAME VARCHAR2(20) NOT NULL,
	LOCATION VARCHAR2(20)
);

COMMENT ON TABLE DEPARTMENTS IS '부서테이블';

COMMENT ON COLUMN DEPARTMENTS.DEPTNO IS '부서번호';

COMMENT ON COLUMN DEPARTMENTS.DEPTNAME IS '부서이름';

COMMENT ON COLUMN DEPARTMENTS.LOCATION IS '부서위치';

CREATE UNIQUE INDEX PK_DEPARTMENTS
	ON DEPARTMENTS (
		DEPTNO ASC
	);

ALTER TABLE DEPARTMENTS
	ADD
		CONSTRAINT PK_DEPARTMENTS
		PRIMARY KEY (
			DEPTNO
		);

CREATE TABLE EMPLOYEES (
	EMPNO NUMBER NOT NULL,
	EMPNAME VARCHAR2(20) NOT NULL,
	SSN VARCHAR2(13) NOT NULL,
	MOBILE VARCHAR2(20),
	REG_DATE DATE DEFAULT sysdate,
	DEPTNO NUMBER
);

